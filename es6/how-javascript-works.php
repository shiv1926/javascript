<h4>Introduction</h4>
<ul>
    <li>JavaScript is a very powerful client-side scripting language.</li>
    <li>JavaScript is used mainly for enhancing the interaction of a user with the webpage. In other words, you can make your webpage more lively and interactive, with the help of JavaScript. JavaScript is also being used widely in game development and Mobile application development.</li>
    <li>Today, JavaScript can execute not only in the browser, but also on the server, or actually on any device that has a special program called the JavaScript engine.</li>
    <li>The browser has an embedded engine sometimes called a “JavaScript virtual machine”.</li>
</ul>
<h4>JavaScript Engines</h4>
<ul>
    <li>There are different JavaScript Engine depending on the browser you use. For example, Chrome has V8, Internet Explorer has Chakra, FireFox as Spidermonkey, and Safari has JavaScriptCore. All of these JavaScript engines helps your browser by supporting memory management, garbage collection, in-time compilation and much more.</li>
</ul>
<h4>How do engines work?</h4>
<ul>
    <li>Engines are complicated. But the basics are easy.</li>
    <li>The engine (embedded if it’s a browser) reads (“parses”) the script.</li>
    <li>Then it converts (“compiles”) the script to the machine language.</li>
    <li>And then the machine code runs, pretty fast.</li>
    <li>The engine applies optimizations at each step of the process. It even watches the compiled script as it runs, analyzes the data that flows through it, and applies optimizations to the machine code based on that knowledge. When it’s done, scripts run quite fast.</li>
</ul>
